            <h3>Como Funciona</h3>
            <p>Títulos públicos são contratos financeiros emitidos pelo governo para captar recursos junto a
                investidores. Ao adquirir um título, o investidor está, na prática, emprestando dinheiro ao Estado em
                troca de uma remuneração futura.</p>
            <p>O Tesouro Direto funciona como uma alternativa de investimento em renda fixa, na qual o investidor
                empresta recursos financeiros ao governo e recebe uma remuneração previamente acordada ou indexada a
                indicadores econômicos, como a taxa Selic ou a inflação (IPCA).</p>

            <h3>Tipos de Títulos</h3>
            <ul>
