                    anos.</li>
                <li><strong>Prefixados</strong>: Juros fixos definidos na compra, garantindo previsibilidade no retorno.
                </li>
                <li><strong>Tesouro Selic</strong>: Pós-fixado, atrelado à taxa Selic, ideal para iniciantes.</li>
                <li><strong>Tesouro IPCA</strong>: Rentabilidade atrelada à inflação, com ganho real garantido.</li>
            </ul>

